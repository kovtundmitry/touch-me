package tachos.ru.touch_me.data;

public class Users {
    private String objectId;
    private String email;
    private String name;
    private long lastOnline;

    public String getObjectId() {
        return objectId;
    }

    public String getEmail() {
        return email;
    }

    public String getName() {
        return name;
    }

    public long getLastOnline() {
        return lastOnline;
    }
}
